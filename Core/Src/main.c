/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const * argument)
{
  /* init code for LWIP */
  MX_LWIP_Init();
  /* USER CODE BEGIN 5 */
  static uint32_t lifetime = 0;
  static uint8_t ledON = 1;  
  /* Infinite loop */
  for(int i=0;;i++)
  {
    osDelay(1);
    if(i>1000)
    {
        i = 0;
        if( !(lifetime++%3) )
        {
            // Refresh states for XCP Measures
            if (xSemaphoreTake(xcpStateAccessMutexHandle, portMAX_DELAY) == pdTRUE)
            {
                // Critical section
                RefreshState();
                // Give the mutex back
                xSemaphoreGive(xcpStateAccessMutexHandle);
            }
        }
        else if( !(lifetime++%30) )
        {
            printf("Still Alive after %d second(s), ", lifetime);

            HAL_GPIO_WritePin(GPIOB, GPIO_PIN_7, ledON++%2 );    //Blue LED ON/OFF  
            dhcp_status_trace();
            puts("");

            float value = GetTemperature();
            // Convert float to integer parts
            int whole = (int)value;
            int frac = (int)((value - whole) * 1000);
            printf("Temperature=%d.%02d, user button state=%d\n", whole, frac, GetButtonState());

        }
    }
  }
  /* USER CODE END 5 */
}

